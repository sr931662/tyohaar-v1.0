# ── Regex helpers ──────────────────────────────────────────────────────────────

_PHONE_RE = re.compile(r"^\+[1-9]\d{9,14}$")
_EMAIL_RE = re.compile(r"^[^@\s]+@[^@\s]+\.[^@\s]+$")

# ── Annotated scalar types ─────────────────────────────────────────────────────

OTPIdentifier = Annotated[
    str,
    Field(
        min_length=5,
        max_length=320,
        description=(
            "Phone number (E.164, e.g. +919876543210) "
            "or email address used to receive the OTP."
        ),
    ),
]
"""
Polymorphic identifier: accepts either a valid E.164 phone number or
an RFC-5322 email address. Validated at the schema layer via
`validate_otp_identifier`.
"""

OTPCode = Annotated[
    str,
    Field(
        min_length=4,
        max_length=8,
        pattern=r"^\d{4,8}$",
        description="Numeric OTP code submitted by the user (4–8 digits).",
    ),
]
"""Raw OTP string submitted by the end-user for verification."""


def validate_otp_identifier(value: str) -> str:
    """
    Ensure `value` is either a valid E.164 phone number or an email address.

    Raises ValueError on invalid input so Pydantic surfaces a clean 422.
    """
    stripped = value.strip()
    if _PHONE_RE.match(stripped) or _EMAIL_RE.match(stripped):
        return stripped
    raise ValueError(
        "identifier must be a valid E.164 phone number (e.g. +919876543210) "
        "or an email address."
    )


