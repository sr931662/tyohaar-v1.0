class DeviceInfoSchema(BaseSchema):
    """
    Device context attached to a session or OTP request.

    All fields are optional because some platforms (e.g. web) may not
    supply the full device fingerprint.
    """

    device_id: str | None = Field(
        default=None,
        max_length=256,
        description="Stable device identifier generated by the client SDK.",
    )
    device_name: str | None = Field(
        default=None,
        max_length=200,
        description="Human-readable device name (e.g. 'iPhone 15 Pro').",
    )
    device_type: DeviceType | None = Field(
        default=None,
        description="Physical form factor of the client device.",
    )
    platform: Platform | None = Field(
        default=None,
        description="OS platform of the client device.",
    )
    os: str | None = Field(default=None, max_length=50)
    os_version: str | None = Field(default=None, max_length=50)
    browser: str | None = Field(default=None, max_length=100)
    browser_version: str | None = Field(default=None, max_length=100)
    app_version: str | None = Field(default=None, max_length=50)
    user_agent: str | None = Field(default=None, max_length=512)


class LocationInfoSchema(BaseSchema):
    """
    Geo-location context derived from IP or GPS for session records.

    This is informational only; never used for access-control decisions.
    """

    ip_address: str | None = Field(default=None, max_length=45)
    country_code: str | None = Field(default=None, max_length=2)
    region: str | None = Field(default=None, max_length=100)
    city: str | None = Field(default=None, max_length=100)
    timezone: str | None = Field(default=None, max_length=64)
    language: str | None = Field(default=None, max_length=20)
